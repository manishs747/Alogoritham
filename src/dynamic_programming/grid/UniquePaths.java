    /**
     * https://leetcode.com/problems/unique-paths/
     * @param m
     * @param n
     * @return
     */

    public static int uniquePaths(int m, int n) {
        return uniquePaths(0,0,m,n,new int [m][n]);
    }

    public static int uniquePaths(int row,int col,int m, int n,int [][] memo) {
        if(!isValid(row,col,m,n)) return 0;
        if(isAtEnd(row,col,m,n)) return 1;
        if(memo[row][col] == 0){
            memo[row][col] = uniquePaths(row+1,col,m,n,memo) +uniquePaths(row,col+1,m,n,memo);
        }
        return memo[row][col];
    }

    private static boolean isAtEnd( int row, int col,int m ,int n ) {
        return row == m-1 && col == n-1 ;
    }

    private static boolean isValid( int row, int col,int m ,int n) {
        return row < m && col < n ;
    }


